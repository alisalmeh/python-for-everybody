# Assume that we execute the following assignment statements:
width = 17
height = 12.0

# For each of the following expressions, write the value of the expression 
# and the type (of the value of the expression).

# 1. width//2
result1 = width // 2
print('result1 is:', result1)
print('Type of result1:', type(result1))

# 2. width/2.0
result2 = width / 2.0
print('\nresult2 is:', result2)
print('Type of result2:', type(result2))

# 3. height/3
result3 = height / 3
print('\nresult3 is:', result3)
print('Type of result3:', type(result3))

# 4. 1 + 2 * 5
result4 = 1 + 2 * 5
print('\nresult4 is:', result4)
print('Type of result4:', type(result4))